
class TarjetaCredito():

    def __init__(self,nombre,numeroTarjeta,codigo,tipoTarjeta):
        self.nombre=nombre
        self.numeroTarjeta=numeroTarjeta
        self.codigo=codigo
        self.tipoTarjeta=tipoTarjeta
        

    def getNombre(self):
        return self.nombre
    
    def getNumeroTarjeta(self):
        return self.numeroTarjeta

    def getCodigo(self):
        return self.codigo

    def getTipoTarjeta(self):
        return self.tipoTarjeta





    